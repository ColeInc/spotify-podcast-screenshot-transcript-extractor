// Handle visibility changes to pause animation when tab is not visible
document.addEventListener('visibilitychange', () => {
    if (document.hidden && window.asciiAnim) {
        window.asciiAnim.stop();
    } else if (!document.hidden && window.asciiAnim) {
        window.asciiAnim.start();
    }
});

// Initialize animation when DOM is ready
window.addEventListener('DOMContentLoaded', () => {
    window.asciiAnim = new ASCIIAnimation();
});
